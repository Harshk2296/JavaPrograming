package com.collegePrograms.MCA1;

public class RadiusOfCircle {
	
	public double area(double radius) {
		double PI= 3.14;
		double radSqaure=radius*radius;
		double Area=PI*radSqaure;
		
		return Area;
		
	}
	

}
