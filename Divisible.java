//Accept one number and check whether is divisible by 7 or not
package com.collegePrograms.MCA1;

public class Divisible {

	public void checkDivisible(int number) {

		if (number % 7 == 0) {
			System.out.println(number + " is divisible by 7");
		}else {
			System.out.println(number + " number is not divisible by 7");
		}
		
	}

}
