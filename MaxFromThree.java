//Develop a program to find the maximum number between two given number
package com.collegePrograms.MCA1;

public class MaxFromThree {

	public void maxFromThree(int number1, int number2, int number3) {
		if(number1>number2) {
			System.out.println(number1+" Number 1 is Max");
		}
	else if (number2>number3) {
		System.out.println(number2 + " Number 2 is Max");
	} else {
		System.out.println(number3+" Number 3 is Max");
	}
		
	}
	

}
